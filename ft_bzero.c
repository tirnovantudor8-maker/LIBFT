#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*point;
	size_t			i;

	i = 0;
	point = (unsigned char *)s;
	while (i < n)
	{
		point[i] = 0;
		i++;
	}
}
